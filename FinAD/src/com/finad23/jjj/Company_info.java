package com.finad23.jjj;

public class Company_info {
	private String id, pw, comname, name, comnumber, comemail, comlicense;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}

	public String getComname() {
		return comname;
	}

	public void setComname(String comname) {
		this.comname = comname;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getComnumber() {
		return comnumber;
	}

	public void setComnumber(String comnumber) {
		this.comnumber = comnumber;
	}

	public String getComemail() {
		return comemail;
	}

	public void setComemail(String comemail) {
		this.comemail = comemail;
	}

	public String getComlicense() {
		return comlicense;
	}

	public void setComlicense(String comlicense) {
		this.comlicense = comlicense;
	}
}
